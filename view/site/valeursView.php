                <strong style="color:#71310D;">Pourquoi ?</strong>
                <br><br>
                Façonner des écosystèmes équilibrés par la biodiversité permet la connexion de l’humain à son terroir, moyennant son aggradation : c’est la définition de l’agroécologie.<br>
                Cela consiste à distinguer des dynamiques locales vertueuses, conjuguant viabilité économique, préservation voire restauration du foncier naturel, autonomie et résilience énergétique, création de liens et de valeur, dans le respect de l’humain et son territoire.<br> C’est un beau programme, n’est-ce pas ? Dans l’absolu, il emporterait sans mal l’adhésion de tous. 
                Même ceux qui doutent, pour peu qu’on leur explique les tenants et les aboutissants de chacune de ces pistes, devraient pouvoir se laisser séduire !
                <br><br>
                <strong style="color:#71310D;">Comment ?</strong>
                <br><br>            
                C’est pourquoi <strong class="egp">EARTH Garden parc</strong><i> d’exposition & source d’inspiration</i>, face aux enjeux de la transition écologique, s’engage en faveur de la promotion de <strong>l’agroécologie, de l’agroforesterie, de la permaculture, du développement durable, et des énergies renouvelables.</strong>
                Nous nous appuyons sur une dynamique d’échanges, de mutualisation des énergies vives, de partage et de mise en réseau, pour établir des liens entre les différents acteurs, mettre en relation les producteurs et les consommateurs en créent une synergie intérêt mutuel, en incluant aussi le retour à l’emploi des personnes précaires.
